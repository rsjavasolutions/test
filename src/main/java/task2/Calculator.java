package task2;

public class Calculator {

    public int sum(int a, int b){
        return a + b;
    }

    public int subtract(int a, int b){
        return a -b;
    }

    public int multiply(int a, int b){
        return a * b;
    }

    public double divide(double a, double b){
        return a / b;
    }

}
